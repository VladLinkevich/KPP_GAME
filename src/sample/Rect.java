package sample;

class Rect {

    double x;
    double y;
    double h;
    double w;

    Rect() {
        x = y = h = w = 0;
    }

    Rect(final double x, final double y, final double h, final double w) {
        this.x = x;
        this.y = y;
        this.h = h;
        this.w = w;
    }
}
